function renderStats(data) {
    const total = data.length;
    
    const completados8 = data.filter(d => d.informe_8 === "Completado").length;
    const completadosHidrico = data.filter(d => d.diag_hidrico === "Completado").length;
    const completadosFoto = data.filter(d => d.anteproyecto_foto === "Completado").length;
    
    const pct8 = Math.round((completados8 / total) * 100);
    const pctHidrico = Math.round((completadosHidrico / total) * 100);
    const pctFoto = Math.round((completadosFoto / total) * 100);
    
    document.getElementById('stat-rep8').innerText = pct8 + '%';
    document.getElementById('stat-hidrico').innerText = pctHidrico + '%';
    document.getElementById('stat-foto').innerText = pctFoto + '%';
    
    document.getElementById('bar-rep8').style.width = pct8 + '%';
    document.getElementById('bar-hidrico').style.width = pctHidrico + '%';
    document.getElementById('bar-foto').style.width = pctFoto + '%';
}

function renderTable(data) {
    const tbody = document.getElementById('table-body');
    tbody.innerHTML = '';
    
    data.forEach(item => {
        const tr = document.createElement('tr');
        
        tr.innerHTML = `
            <td data-label="Productor">
                <div style="display: flex; flex-direction: column; align-items: flex-end; width: 100%; word-break: break-word;">
                    <div class="empresa-name">${item.empresa}</div>
                    <div class="empresa-region">${item.region}</div>
                </div>
            </td>
            <td data-label="Inf. 8">${getStatusBadge(item.informe_8)}</td>
            <td data-label="Diag. Hídrico">${getStatusBadge(item.diag_hidrico)}</td>
            <td data-label="Foto">${getStatusBadge(item.anteproyecto_foto)}</td>
            <td data-label="Acción">
                <button class="action-btn" onclick="openModal(${item.id})">Ver Detalle</button>
            </td>
        `;
        tbody.appendChild(tr);
    });
}

function getStatusBadge(status) {
    let cssClass = '';
    let icon = '';
    if(status === 'Completado') {
        cssClass = 'status-completado';
        icon = '✅';
    } else if (status === 'En Proceso') {
        cssClass = 'status-proceso';
        icon = '⏳';
    } else {
        cssClass = 'status-pendiente';
        icon = '❌';
    }
    return `<span class="badge ${cssClass}">${icon} ${status}</span>`;
}

function setupFilters() {
    const filterSelect = document.getElementById('region-filter');
    filterSelect.addEventListener('change', (e) => {
        const region = e.target.value;
        if(region === 'Todas') {
            renderTable(beneficiariesData);
            renderStats(beneficiariesData);
        } else {
            const filtered = beneficiariesData.filter(d => d.region === region);
            renderTable(filtered);
            renderStats(filtered);
        }
    });
}

function openModal(id) {
    const item = beneficiariesData.find(d => d.id === id);
    if(!item) return;

    document.getElementById('modal-empresa-nombre').innerText = item.empresa;
    document.getElementById('modal-empresa-region').innerText = item.region + " | RUT: " + item.rut;

    document.getElementById('detalle-modal').style.display = 'flex';
}

function closeModal() {
    document.getElementById('detalle-modal').style.display = 'none';
}


